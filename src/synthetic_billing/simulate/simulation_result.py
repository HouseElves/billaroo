"""Result of a complete monthly simulation run (D40, D46).

A :class:`SimulationResult` carries the final
:class:`SimulationState` after all months have advanced, together with
every lifecycle event emitted along the way, and (since D46) every
invoice header and invoice line generated by run-level recurring
billing — all in deterministic emission order.

This is the simulation-level result envelope, distinct from
:class:`synthetic_billing.actions.action_protocols.ActionResult`: an
``ActionResult`` is the result of applying one semantic action or
chain to one state; a :class:`SimulationResult` is the cumulative
record of running the monthly driver over the full configured horizon.

The billing collections are explicit, typed tuples rather than a
generic emitted-record container (constitution rule 22, mirroring the
D43 ``ActionResult`` widening): the run produces invoice headers and
invoice lines, so those are the two collections the envelope grows.
"""

from __future__ import annotations

import dataclasses
from typing import ClassVar

from synthetic_billing._validation import (
    CheckSpec,
    CheckTuple,
    _Validated,
    collection_element_checks,
)
from synthetic_billing.contracts.event_contracts import LifecycleEvent
from synthetic_billing.contracts.invoice_contracts import Invoice, InvoiceLine
from synthetic_billing.simulate.simulation_state import SimulationState

__all__ = ["SimulationResult"]


# Hoisted out of the class body so the type-spec declaration is a
# single reference rather than an inlined literal that visually mirrors
# the action-layer envelope's spec.
_RESULT_TYPE_SPECS: tuple[CheckSpec, ...] = (
    ("state", SimulationState),
    ("lifecycle_events", tuple),
    ("invoices", tuple),
    ("invoice_lines", tuple),
)


@dataclasses.dataclass(frozen=True)
class SimulationResult(_Validated):
    """The terminal state, ordered event log, and billing of a run.

    Attributes:
        state: The final :class:`SimulationState` after the last
            simulated month has been applied.
        lifecycle_events: The lifecycle events emitted during the run,
            in deterministic emission order (month-major; within each
            month in stable subscriber order).  Empty when the run
            performed no transitions.
        invoices: The invoice headers generated during the run, in
            deterministic emission order (month-major; within each
            month in stable account order).  Empty when no account-month
            was chargeable (D46).
        invoice_lines: The invoice lines generated during the run, in
            deterministic emission order.  Each line belongs to one of
            the run's ``invoices`` and the lines of a single invoice
            preserve the account-month billing operation's order
            (D44, D46).

    This envelope carries billing output but does not, by itself,
    reconcile it: the invariants that every line references an invoice
    in the same result, that each invoice total equals the sum of its
    lines, and that no account-month yields two invoices are established
    by the run-level billing integration that fills these fields (D46),
    not re-proved here.
    """

    state: SimulationState
    lifecycle_events: tuple[LifecycleEvent, ...]
    invoices: tuple[Invoice, ...]
    invoice_lines: tuple[InvoiceLine, ...]

    _type_check_specs: ClassVar[tuple[CheckSpec, ...]] = _RESULT_TYPE_SPECS

    # SimulationResult (per-run) and ActionResult (per-action) are
    # deliberately distinct types that happen to carry the same
    # state-plus-billing-collections shape (D40 keeps them separate even
    # when their fields coincide).  The per-element checking is shared
    # vocabulary (``collection_element_checks``); the field declarations
    # and this assembly are the irreducible residue of two intentionally
    # separate envelopes, so the duplicate-code report on this validated
    # shape is suppressed locally rather than collapsed into a shared
    # base class that D40 rejects.
    def _structural_checks(  # pylint: disable=duplicate-code
        self,
    ) -> tuple[CheckTuple, ...]:
        """Return structural validation checks for this run result.

        Re-checks ``state`` and validates each output collection with
        independent, rule-23-safe per-element checks so a
        directly-constructed (test-only) instance still reports its
        violations.
        """
        checks: list[CheckTuple] = [
            (isinstance(self.state, SimulationState), "state", self.state),
        ]
        checks.extend(
            collection_element_checks(
                "lifecycle_events", self.lifecycle_events, LifecycleEvent,
            )
        )
        checks.extend(
            collection_element_checks("invoices", self.invoices, Invoice)
        )
        checks.extend(
            collection_element_checks(
                "invoice_lines", self.invoice_lines, InvoiceLine,
            )
        )
        return tuple(checks)
